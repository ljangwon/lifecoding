							<p style="text-align: center;"><span style="font-size:22px;"><strong><span style="color:#008000;">내맘대로 가드닝</span></strong></span></p>

<p style="text-align: center;"><em style="font-size: 13.2px; line-height: 23.76px; text-align: center;"><span style="font-size: 14px;">내맘대로 가드닝에 오신 여러분을 환영합니다.</span></em></p>

<p style="text-align:center"><img alt="수경재배" height="225" src="https://s3-ap-northeast-2.amazonaws.com/opentutorials-user-file/course/2304/4405.png" width="225" /></p>

<p style="font-size: 13.2px; line-height: 23.76px;"><span style="font-size:14px;">자투리 공간에서 직접 작물을 길러봅시다. </span></p>

<p style="font-size: 13.2px; line-height: 23.76px;"><span style="font-size:14px;">내맘대로 가드닝은 토경과 수경을 사용해서 식물을 기르는 법을 정리하고 배우는 공간입니다. 식물을 기르는 데에는 여러가지 환경과 방법이 있을 수 있겠지만, 이 곳에서는&nbsp;주로 <strong>실내</strong>에서 <strong>소규모</strong>로 식물을 수경 재배하는 것에 초점을 맞추고 있습니다.&nbsp;</span></p>

<p style="font-size: 13.2px; line-height: 23.76px;">&nbsp;</p>

<hr />
<p>&nbsp;</p>

<p><span style="font-size:14px;">이곳에서는 이런 것들을 배울 수 있습니다.</span></p>

<p>&nbsp;</p>

<ul>
	<li><span style="font-size:14px;"><a href="https://opentutorials.org/course/2306"><strong>수경재배101</strong></a>&nbsp;- 처음 텃밭재배를 시작하는 초보자를 위한 강좌.&nbsp;간단한 수경재배 시스템으로 캣닙을&nbsp;길러보면서 기본적인 원리를 익힐 수 있습니다.&nbsp;</span></li>
	<li><span style="font-size:14px;"><a href="https://opentutorials.org/course/2307"><strong>수경재배 시스템</strong></a> - 수경재배의 역사와&nbsp;다양한 수경재배 방식&nbsp;정리.</span></li>
	<li><span style="font-size:14px;"><strong><a href="https://opentutorials.org/course/2334">양액 관련 정보</a> - </strong>pH, 농도, 온도, 양액 조성 관련 정보</span></li>
	<li><span style="font-size:14px;"><a href="https://opentutorials.org/course/2409"><strong>조명 관련 정보</strong></a> - 식물 생장을 보조해주는 led 관련 정보.</span></li>
	<li><span style="font-size:14px;"><a href="https://opentutorials.org/course/2411"><strong>배지 관련 정보</strong></a> - 펄라이트, 황토볼, 코코슬라브 등 고형배지에 관한 설명.</span></li>
	<li><span style="font-size:14px;"><a href="https://opentutorials.org/course/2308"><strong>참고할 만한 프로젝트 예제</strong></a> - 컵, 박스 등을 활용한 다양한 프로젝트 예제.&nbsp;&nbsp;</span></li>
	<li><span style="font-size:14px;"><a href="https://opentutorials.org/course/2309"><strong>작물 별 텃밭도감</strong></a>&nbsp;- 각종 쌈채소, 열매채소, 뿌리채소, 허브의 특징과 기르는 법 정리.</span></li>
	<li><span style="font-size: 14px;"><a href="https://opentutorials.org/course/2823"><strong>홉 프로젝트</strong></a> : 자가양조 맥주를 만들 때 필요한 홉을 직접 길러봅니다.&nbsp;</span></li>
</ul>

